    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String chat = JOptionPane.showInputDialog(this, "Gửi thông báo đến toàn server\n",
                "Thông Báo Server", JOptionPane.INFORMATION_MESSAGE);
        if (chat != null) {
            Message msg = new Message(93);
            try {
                msg.writer().writeUTF(chat);
            } catch (IOException ex) {
                java.util.logging.Logger.getLogger(Panel.class.getName()).log(Level.SEVERE, null, ex);
            }
            Service.getInstance().sendMessAllPlayer(msg);
            msg.cleanup();
            Logger.log(Logger.PURPLE, "Thông báo: " + chat + "\n");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

